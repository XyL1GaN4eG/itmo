#!/bin/bash

# Функция для рекурсивного обхода директорий
function recursive_cat {
    local dir="$1"
    
    # Проходим по всем элементам в текущей директории
    for item in "$dir"/*; do
        if [ -d "$item" ]; then
            # Если это директория, вызываем функцию рекурсивно
            recursive_cat "$item"
        elif [ -f "$item" ]; then
            # Если это файл, выводим его имя и содержимое
            echo "Файл: $item"
            cat "$item"
            echo "" # Пустая строка для разделения вывода разных файлов
        fi
    done
}

# Начинаем с указанной директории, или текущей директории если не указана
start_dir="${1:-.}"
recursive_cat "$start_dir"
